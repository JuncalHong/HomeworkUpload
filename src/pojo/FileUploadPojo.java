package pojo;

public class FileUploadPojo {
	String localDirectoryAndFileName;
	String ftpFileName;
	String ftpDirectory;
	public String getLocalDirectoryAndFileName() {
		return localDirectoryAndFileName;
	}
	public void setLocalDirectoryAndFileName(String localDirectoryAndFileName) {
		this.localDirectoryAndFileName = localDirectoryAndFileName;
	}
	public String getFtpFileName() {
		return ftpFileName;
	}
	public void setFtpFileName(String ftpFileName) {
		this.ftpFileName = ftpFileName;
	}
	public String getFtpDirectory() {
		return ftpDirectory;
	}
	public void setFtpDirectory(String ftpDirectory) {
		this.ftpDirectory = ftpDirectory;
	}
	
}
